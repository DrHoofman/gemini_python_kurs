# allgemeine Syntx für Funktionen
# def funktionsname(parameter):
#     #Codeblock
#     return wert
#	•	def: Schlüsselwort zum Definieren einer Funktion.
#	•	funktionsname: Name der Funktion.
#	•	parameter: Optional, Werte, die der Funktion übergeben werden.
#	•	return: Optional, gibt einen Wert zurück.

#Einfache Funktionen ohne Parameter
def hallo():
    print("Hallo, Stefan!")

# Funktionsaufruf
hallo()